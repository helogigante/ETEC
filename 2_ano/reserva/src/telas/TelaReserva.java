    private void txtcodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcodigoKeyPressed
        if(evt.getKeyCode()==evt.VK_ENTER){
            int codigo = Integer.parseInt(txtcodigo.getText());
            int status=0;
            for(int i=0; i<cont; i++){
                if(lista[i].getCodigo()==codigo){
                    status=1;
                    txtdata.setText(lista[i].getData());
                    txthorario.setText(lista[i].getHora());
                    txtlocal.setText(lista[i].getLocal());
                    txtnome.setText(lista[i].getNome());
                    txtclassificacao.setText(lista[i].getClassificacao());
                }
            }
            if(status==0) JOptionPane.showMessageDialog(null,"Evento nâo foi encontrado!");
        }
    }//GEN-LAST:event_txtcodigoKeyPressed

