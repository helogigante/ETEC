    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        if(cont<10){
            Reserva re = new Reserva();
            re.setCpf(txtcpf.getText());
            re.setNome(txtnnome.getText());
            re.setData(txtdt.getText());
            re.setTele(txttele.getText());
            re.setEmail(txtemail.getText());
            lista1[cont1]=re;
            cont1++;
                DefaultTableModel model = new DefaultTableModel();
                model=(DefaultTableModel)tbdados.getModel();
                model.addRow(new Object[]{re.getCpf(), re.getNome(), re.getData(), re.getTele(), re.getEmail()});
                tbdados.setModel(model);
        } else{
            JOptionPane.showMessageDialog(null,"Não é possível cadastrar.");
        }
        limpar();
    }//GEN-LAST:event_btnsalvarActionPerformed

    private void limpar(){
        txtcpf.setText(null);
        txtnnome.setText(null);
        txtdt.setText(null);
        txttele.setText(null);
        txtemail.setText(null);
        
        txtcodigo.setText(null);
        txtnome.setText(null);
        txtdata.setText(null);
        txthorario.setText(null);
        txtlocal.setText(null);
        txtclassificacao.setText(null);
    }
    
    private void carregaTabela(){
        DefaultTableModel model = (DefaultTableModel) tbdados.getModel();
        for(int i=0; i<model.getRowCount(); i++){
            model.removeRow(i);
        }
        for(int x=0; x<cont1; x++){
            model.addRow(new Object[]{lista1[x].getCpf(), lista1[x].getNome(), lista1[x].getData(), lista1[x].getTele(), lista1[x].getEmail()});
            tbdados.setModel(model);
        }
    }
    
