    private void CarregaLinha(){
        lin = new Linha(0,"");
        cbxlinha.addItem(new Linha(0, "Escolha a linha"));
        try{
            ResultSet rs = lin.Pesquisar();
            while(rs.next()){
                cbxlinha.addItem(new Linha(rs.getInt("tb01_id"),rs.getString("tb01_descricao")));
            }
        }catch (SQLException ex){
            System.out.println("Erro "+ ex);
        }
    }
    
    private void CarregaTarifa(){
        tar = new Tarifa();
        ConverteMoeda cm = new ConverteMoeda();
        try{
            ResultSet rs = tar.Pesquisar();
            if(rs.next()){
                txtvalor.setText(cm.ConvMoeda(rs.getString("tb04_valor")));
            }
        }catch (SQLException ex){
            System.out.println("Erro: "+ex);
        }
    }
    
    private void CarregaBilhete(){
        bil = new Bilhete();
        try{
            ResultSet rs = bil.Pesquisar();
            if(rs.next()){
                txtbilhete.setText((rs.getString("tb03_num_bi")));
            }
        }catch (SQLException ex){
            System.out.println("Erro: "+ex);
        }
    }

