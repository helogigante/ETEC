    private void cbxlinhaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxlinhaItemStateChanged
        est = new Estacao(0,"Escolha a linha");
        cbxestacao.removeAllItems();
        cbxestacao.addItem(est);
        lin = (Linha)cbxlinha.getSelectedItem();
        est.setId_linha(lin.getId());
        if(lin.getId()!=0){
            try{
                ResultSet rs = est.Pesquisar();
                while(rs.next()){
                    cbxestacao.addItem(new Estacao(rs.getInt("tb02_id"),rs.getString("tb02_nome")));
                }
            }catch(SQLException ex){
                System.out.println("Erro: "+ex);
            }
        }
    }//GEN-LAST:event_cbxlinhaItemStateChanged

