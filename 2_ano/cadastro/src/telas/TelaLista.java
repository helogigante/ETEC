    private void carregaTabela(){
        String sql = "select * from tb01_login";
        try {
            con.conectar();
            ResultSet rs = con.selectSQL(sql);      
            DefaultTableModel model = (DefaultTableModel) tbdados.getModel();
            model.setRowCount(0);
            
            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getString("tb01_id"),
                    rs.getString("tb01_usuario"),
                    rs.getString("tb01_email"),
                    rs.getString("tb01_senha"),
                });
            }
            con.desconectar();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaLista.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TelaLista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
