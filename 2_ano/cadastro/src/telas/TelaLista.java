    private void tbdadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbdadosMouseClicked
        int selectedRowIndex = tbdados.getSelectedRow();
        
        if (selectedRowIndex != -1) {
            Object id = tbdados.getValueAt(selectedRowIndex, 0);
            Object user = tbdados.getValueAt(selectedRowIndex, 1);
            Object email = tbdados.getValueAt(selectedRowIndex, 2);
            
            txtid.setText((String) id);
            txtusuario.setText((String) user);
            txtemail.setText((String) email);
        }
    }//GEN-LAST:event_tbdadosMouseClicked

