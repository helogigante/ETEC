    private void btnatualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnatualizarActionPerformed
        String sql="update tb01_login set tb01_usuario='"+txtusuario.getText()+"', tb01_email='"+txtemail.getText()+"', tb01_senha='"+txtsenha.getText()+
            "' where tb01_id='"+txtid.getText()+"'";
        try {
            con.conectar();
            if(con.runSQL(sql)==1){
                JOptionPane.showMessageDialog(null,"Atulizado com sucesso!");
            }else{
                JOptionPane.showMessageDialog(null,"Não foi possível atualizar!");
            }
            con.desconectar();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaLista.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TelaLista.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtid.setText("");
        txtusuario.setText("");
        txtemail.setText("");
        txtsenha.setText("");
        carregaTabela();
    }//GEN-LAST:event_btnatualizarActionPerformed

