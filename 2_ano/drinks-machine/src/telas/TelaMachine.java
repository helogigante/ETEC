    private void destravar() {
        String sql = "select * from tb01_produtos";
        con = new Conexao();
        try {
            con.conectar();
            ResultSet rs = con.selectSQL(sql);
            int i = 0;
            while (rs.next()) {
                int quantidade = rs.getInt("tb01_qtde");
                if (dinheiro >= preco[i] && quantidade > 0) {
                    btnescolha[i].setEnabled(true);
                } else {
                    btnescolha[i].setEnabled(false);
                    if (quantidade <= 0) {
                        lbfotos[i].setIcon(null);
                    }
                }
                i++;
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaMachine.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TelaMachine.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
