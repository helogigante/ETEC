    private void carregaprodutos(){
        dinheiro=0;
        lbfotos=new JLabel[]{lbimg1,lbimg2,lbimg3,lbimg4,lbimg5,lbimg6,lbimg7,lbimg8,lbimg9};
        lbprecos=new JLabel[]{lbpreco1,lbpreco2,lbpreco3,lbpreco4,lbpreco5,lbpreco6,lbpreco7,lbpreco8,lbpreco9};
        btnescolha=new JButton[]{btn1,btn2,btn3,btn4,btn5,btn6,btn7,btn8,btn9};
        lbcedulas=new JLabel[]{lbcedula2,lbcedula5,lbcedula10,lbcedula20,lbcedula50};
        for(int i=0; i<5; i++){
            ImageIcon imagem = new  ImageIcon(this.getClass().getResource("/img/ced"+(i+1)+".png"));       
            Image imag = imagem.getImage().getScaledInstance(lbcedulas[i].getWidth(), lbcedulas[i].getHeight(), Image.SCALE_DEFAULT); 
            lbcedulas[i].setIcon(new ImageIcon(imag));
        }
        /* carregar imagem da pasta
        for(int i=0; i<9; i++){
            ImageIcon imagem = new  ImageIcon(this.getClass().getResource("/img/bebi"+(i+1)+".png"));       
            Image imag = imagem.getImage().getScaledInstance(lbfotos[i].getWidth(), lbfotos[i].getHeight(), Image.SCALE_DEFAULT); 
            lbfotos[i].setIcon(new ImageIcon(imag));
        }
        carregar imagem do banco
        */
        String sql = "select * from tb01_produtos";
        con = new Conexao();
        try {
            con.conectar();
            ResultSet rs = con.selectSQL(sql);
            int i=0;
            while(rs.next()){
                codigo[i]=rs.getInt("tb01_cod");
                preco[i]=rs.getDouble("tb01_preco");
                if(rs.getInt("tb01_qtde")>0){
                    lbprecos[i].setText(cm.ConvMoeda(rs.getString("tb01_preco")));
                    ImageIcon imagem = new  ImageIcon(rs.getBytes("tb01_foto"));
                    Image imag = imagem.getImage().getScaledInstance(lbfotos[i].getWidth(), lbfotos[i].getHeight(), Image.SCALE_DEFAULT); 
                    lbfotos[i].setIcon(new ImageIcon(imag));
                } else {
                    lbprecos[i].setText(null);
                    lbfotos[i].setIcon(null);
                    btnescolha[i].setEnabled(false);
                }
                i++;
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TelaMachine.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TelaMachine.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void movimento(final JLabel fig)
        {             
            new Thread()
            {
                public void run()
                { 
                    Som = java.applet.Applet.newAudioClip(getClass().getResource("/som/saindo.wav")); 
                    Som.stop();
                    Som.play();
                   int y = fig.getLocation().y;
                   int x = fig.getLocation().x;
                   int z = lbdispenser.getLocation().y;            

                    while(y <= z)
                    {
                        y = y + 1; 
                        fig.setLocation(x,y);

                        try
                        {
                            sleep(6);
                        }
                        catch (Exception e)
                        {
                        }
                    }
               
                    lbdispenser.setIcon(fig.getIcon());  
                    fig.setIcon(null);
                }
            }.start();
        }
    
